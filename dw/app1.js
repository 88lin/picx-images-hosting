    function showResult() {
      quizScreen.classList.add('hidden');
      loadingScreen.classList.remove('hidden');
      setTimeout(() => {
        initScores();
        for (let i = 0; i < selectedOptions.length && i < quizData.length; i++) {
          const option = selectedOptions[i];
          const mapRow = scoreMap[i];
          if (!option || !mapRow) continue;
          const questionScores = mapRow[option];
          if (!questionScores) continue;
          for (const dim of dimensionKeys) {
            scores[dim] += (questionScores[dim] || 0);
          }
        }
        const scaledScores = {};
        for (const dim of dimensionKeys) {
          const cap = maxPerDim[dim] || 1;        
          scaledScores[dim] = scores[dim] / cap;  
        }
        function l2Normalize(vec) {
          let sumSq = 0;
          for (const k of dimensionKeys) sumSq += (vec[k] || 0) ** 2;
          const norm = Math.sqrt(sumSq) || 1;
          const out = {};
          for (const k of dimensionKeys) out[k] = (vec[k] || 0) / norm;
          return out;
        }
        const userN = l2Normalize(scaledScores);
        let bestMatchAnimal = null;
        let bestSim = -Infinity;
        for (const name of Object.keys(animalArchetypes)) {
          const aVec = animalArchetypes[name].vector;
          const aN = l2Normalize(aVec);
          let dot = 0;
          for (const k of dimensionKeys) {
            dot += (userN[k] || 0) * (aN[k] || 0);
          }
          if (dot > bestSim) {
            bestSim = dot;
            bestMatchAnimal = name;
          }
        }
        const resultData = animalArchetypes[bestMatchAnimal];
        resultAnimal.textContent = `„Äê ${bestMatchAnimal} „Äë`;
        resultDescription.textContent = resultData.desc;
        saveQuizHistory(bestMatchAnimal, resultData);
        loadingScreen.classList.add('hidden');
        resultScreen.classList.remove('hidden');
        isProcessing = false;
      }, 1500);
    }
	function onTap(el, handler) {
	  if (!el) return;
	  try { el.style.touchAction = 'manipulation'; } catch (e) {}
	  let startX = 0, startY = 0, startTime = 0, downId = null;
	  const MOVE_TOL = 12;
	  const TIME_TOL = 650;
	  const hasPointer = !!window.PointerEvent;
	  if (hasPointer) {
	    el.addEventListener('pointerdown', (e) => {
	      if (!e.isPrimary) return;
	      downId = e.pointerId;
	      startX = e.clientX;
	      startY = e.clientY;
	      startTime = e.timeStamp || performance.now();
	      try { el.setPointerCapture(downId); } catch (err) {}
	    }, { passive: true });
	    el.addEventListener('pointerup', (e) => {
	      if (!e.isPrimary || (downId !== null && e.pointerId !== downId)) return;
	      const t = e.timeStamp || performance.now();
	      const dt = t - startTime;
	      const dx = e.clientX - startX;
	      const dy = e.clientY - startY;
	      const moved = (dx*dx + dy*dy) > (MOVE_TOL*MOVE_TOL);
	      if (dt <= TIME_TOL && !moved) {
	        e.preventDefault();
	        e.stopPropagation();
	        handler(e);
	      }
	      try { el.releasePointerCapture(e.pointerId); } catch (err) {}
	      downId = null;
	    }, { passive: false });
	  } else {
	    el.addEventListener('click', (e) => {
	      e.preventDefault();
	      handler(e);
	    }, { passive: false });
	  }
	}
